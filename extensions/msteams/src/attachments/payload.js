export function buildMSTeamsMediaPayload(mediaList) {
  const first = mediaList[0];
  const mediaPaths = mediaList.map((media) => media.path);
  const mediaTypes = mediaList.map((media) => (media.contentType ?? ""));
  return { MediaPath: first?.path, MediaType: first?.contentType, MediaUrl: first?.path, MediaPaths: (mediaPaths.length > 0) ? mediaPaths : undefined, MediaUrls: (mediaPaths.length > 0) ? mediaPaths : undefined, MediaTypes: (mediaPaths.length > 0) ? mediaTypes : undefined };
}

